def modulo(a, b):
    """
    Zwraca resztę z dzielenia dwóch liczb.

    Args:
        a (int/float): dzielna
        b (int/float): dzielnik (b != 0)

    Returns:
        int/float: reszta z dzielenia a przez b
    """
    return a % b


def multiply(a, b):
    """
    Mnoży dwie liczby.

    Args:
        a (int/float): pierwsza liczba
        b (int/float): druga liczba

    Returns:
        int/float: wynik mnożenia a * b
    """
    return a * b
